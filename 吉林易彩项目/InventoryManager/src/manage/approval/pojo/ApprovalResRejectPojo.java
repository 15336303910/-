package manage.approval.pojo;

import java.io.Serializable;
import java.util.Date;
/**
 * 保存驳回资源信息
 * @author chenqp
 *
 */
public class ApprovalResRejectPojo implements Serializable{
	private Integer id;//主键
	private String resName;//资源名称
	private String resId;//资源主键ID
	private String resType;//资源类型     well人手井       stone标石       pole电杆      buried直埋段    leadup引上     poleLine杆路段  pipe管道段
	private String taskId;//所属工单ID
	private Date  createTime;//创建时间
	private String rejectStr;//驳回说明
	private String deleteFlag;//删除说明
	private String rejectUser;//操作人
	
	public String getDeleteFlag() {
		return deleteFlag;
	}
	public void setDeleteFlag(String deleteFlag) {
		this.deleteFlag = deleteFlag;
	}
	public String getRejectUser() {
		return rejectUser;
	}
	public void setRejectUser(String rejectUser) {
		this.rejectUser = rejectUser;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getResName() {
		return resName;
	}
	public void setResName(String resName) {
		this.resName = resName;
	}
	public String getResId() {
		return resId;
	}
	public void setResId(String resId) {
		this.resId = resId;
	}
	public String getResType() {
		return resType;
	}
	public void setResType(String resType) {
		this.resType = resType;
	}
	public String getTaskId() {
		return taskId;
	}
	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getRejectStr() {
		return rejectStr;
	}
	public void setRejectStr(String rejectStr) {
		this.rejectStr = rejectStr;
	}
}
