package manage.approval.pojo;

import java.io.Serializable;

public class ApprovalListPojo implements Serializable{
	
	private Integer id;//主键
	private String type;//资源类型
	private String motion;//动作
	private String requestId;//易采这的id
	private String responseId;//综资那的id
	private String resSum;//资源总数
	private String resWin;//成功推送的资源
	private String resFeat;//没有成功推送的资源
	private String taskId;//工单实例id
	public String getTaskId() {
		return taskId;
	}
	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getMotion() {
		return motion;
	}
	public void setMotion(String motion) {
		this.motion = motion;
	}
	public String getRequestId() {
		return requestId;
	}
	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}
	public String getResponseId() {
		return responseId;
	}
	public void setResponseId(String responseId) {
		this.responseId = responseId;
	}
	public String getResSum() {
		return resSum;
	}
	public void setResSum(String resSum) {
		this.resSum = resSum;
	}
	public String getResWin() {
		return resWin;
	}
	public void setResWin(String resWin) {
		this.resWin = resWin;
	}
	public String getResFeat() {
		return resFeat;
	}
	public void setResFeat(String resFeat) {
		this.resFeat = resFeat;
	}
	
}
