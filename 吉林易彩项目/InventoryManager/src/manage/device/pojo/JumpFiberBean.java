package manage.device.pojo;

import java.io.Serializable;

/**
 * 跳纤
 * @author chenqp
 *
 */
public class JumpFiberBean implements Serializable{

	private PointCommon start;//起始端端子
	private PointCommon end;//末端端子
	private String discribe;//跳纤业务描述
	
	public String getDiscribe() {
		return discribe;
	}
	public void setDiscribe(String discribe) {
		this.discribe = discribe;
	}
	public PointCommon getStart() {
		return start;
	}
	public void setStart(PointCommon start) {
		this.start = start;
	}
	public PointCommon getEnd() {
		return end;
	}
	public void setEnd(PointCommon end) {
		this.end = end;
	}
	
}
