package manage.user.pojo;

import java.io.Serializable;
import java.util.Date;

/**
 * 短信验证码
 * @author chenqp
 *
 */
public class SmsAuthCodeBean implements Serializable{

	private Integer id;//短信验证码
	private String userName;//登录用户名
	private Date applyTime;//申请时间
	private Date validTime;//有效时间
	private String validLength;//有效时长
	private String  authCode;//生成的验证码
	private String smsContent;//短信内容
	private String status;//状态 0 代表未使用  1代表已使用
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Date getApplyTime() {
		return applyTime;
	}
	public void setApplyTime(Date applyTime) {
		this.applyTime = applyTime;
	}
	public Date getValidTime() {
		return validTime;
	}
	public void setValidTime(Date validTime) {
		this.validTime = validTime;
	}
	public String getValidLength() {
		return validLength;
	}
	public void setValidLength(String validLength) {
		this.validLength = validLength;
	}
	public String getAuthCode() {
		return authCode;
	}
	public void setAuthCode(String authCode) {
		this.authCode = authCode;
	}
	public String getSmsContent() {
		return smsContent;
	}
	public void setSmsContent(String smsContent) {
		this.smsContent = smsContent;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
