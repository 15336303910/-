function color1(v){
	if(v!=null&&v!=""){
		if(v.indexOf('||')==0){
			v = v.substring(2);
			return '<span style="color:#000000;">' + v + '</span>';
		}
		return '<span style="color:#999999;">' + v + '</span>';
	}
}

function grey(v){
	
	return '<span style="color:#999999;">' + v + '</span>';
}

function OperationType(v){
	if(v=="添加"){
		return '<span style="color:#0000FF;">' + v + '</span>';
	}
	if(v=="修改"){
		return '<span style="color:#00FF00;">' + v + '</span>';
	}
	if(v=="删除"){
		return '<span style="color:#FF0000;">' + v + '</span>';
	}
}

